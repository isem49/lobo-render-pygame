# LOOP PRINCIPAL
def main():
    pygame.init()
    display = (700, 700)
    pygame.display.set_mode(display, DOUBLEBUF | OPENGL)

    glClearColor(1.0, 1.0, 1.0, 1.0) # Fundo branco

    gluPerspective(80, (display[0] / display[1]), 1, 20)
    glTranslatef(0, 0, -3)
    glRotatef(0, 0, 0, 0)

    glColor3f(0.0, 0.0, 0.0) # Cor das linhas do lobo (preto)

    while True:
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                pygame.quit()
                quit()

        glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT)
        
        DesenharOlhos() # Chama a nova função para desenhar os olhos preenchidos
        Quadrado()       # Chama a função para desenhar as linhas do lobo

        pygame.display.flip()

main()